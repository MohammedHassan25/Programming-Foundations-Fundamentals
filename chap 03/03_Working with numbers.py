# ---------------------------------------------------------
# Addition in python : +
# Subtraction in python : -
# Multiplication in python : *
# Division in python : /
# The floor Division : // ( ex : 3 // 2 = 1 not 1.5 )
# an exponential operation in python : **
# Remainder of the division : %
# In python, Don't use comma in math
# float number : Any number with a decimal point ( ex : 3.25, 5.5 )
# ---------------------------------------------------------

# integer number

print(2 + 3)
print(2 * 3)
print(2 ** 3)
print(4 - 3)
print(4 / 2)
print(5 // 3)
print(5 % 3)

# float number
print(0.2 + 0.3)
print(0.2 / 0.3)
