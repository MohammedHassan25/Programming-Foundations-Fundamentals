# -----------------------------------------------------------------------------------------------------------------------------
# In computer, Decisions is yes or no, true or false, 0 or 1
# Conditional or Boolean expression : Any expression that breaks down to either true or false ( ex : is equal to 2 * 2 => True
# Relational operators : True or False
# equal in python : ==
# Not equal in python : !=
# greater than sign : >
# less than sign : <
# greater than or equal to sign : >=
# less than or equal to sign : <=
# -----------------------------------------------------------------------------------------------------------------------------

# for example

print(5 == 1)
print(5 != 4)
print(6 > 3)
