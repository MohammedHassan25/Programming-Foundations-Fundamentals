# --------------------------------------------------------------------------------------------------
# When we want to show a message to the user that this is an error we use IF-else statement
# --------------------------------------------------------------------------------------------------

# for example :-

if 5 < 6:
    print("yes, 5 is less than 6")
else:
    print("No, 5 is not less than 6")


name = "Mohammed"
if name == "Mohammed":
    print("Yes, My name is Mohammed")
else:
    print("No, My name is not Mohammed")


name = "Omar"
if name == "Mohammed":
    print("Yes, My name is Mohammed")
else:
    print("No, My name is not Mohammed")
